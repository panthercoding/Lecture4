

public class Nested{
  public static void main(String[] args){

    
    for (int i = 0; i < 5; i++){
      System.out.println("Hi");
      for (int j = 0; j < 2; j++){
        System.out.println("Meow");
      }
    }
    
    for (int i = 0; i < 5; i++){
      System.out.println("Hi");
      System.out.println("Meow");
      System.out.println("Meow");
    }

  }
}
